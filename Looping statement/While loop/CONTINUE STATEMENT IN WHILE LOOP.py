# CONTINUE STATEMENT IN WHILE LOOP
i=1
while i<=20:
    if i%2==0:
        i+=1
        continue
    print(i)
    i+=1
    
print("---------------------------------")

# BREAK STATEMENT IN WHILE LOOP
j=1
while j<=20:
    if j==8:
        break
    print(j)
    j+=1
    
        

